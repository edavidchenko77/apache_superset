# –°–∫—Ä–∏–ø—Ç –¥–ª—è –ø–æ–ª–Ω–æ–π –æ—á–∏—Å—Ç–∫–∏ Docker-–æ–∫—Ä—É–∂–µ–Ω–∏—è –ø—Ä–æ–µ–∫—Ç–∞
# –õ–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–∞—è —Ä–∞–±–æ—Ç–∞ ‚Ññ4: –ê–Ω–∞–ª–∏–∑ –ø—Ä–µ–∑–∏–¥–µ–Ω—Ç–æ–≤ –°–®–ê (–í–∞—Ä–∏–∞–Ω—Ç 30)

